public class PrimitiveOperations {
    public static void main(String[] args) {
        int varOne = 5;
        double varTwo = 3.5;
        System.out.println(varOne);
        System.out.println(varTwo);
        double product = varOne*varTwo;
        System.out.println(product);
        double newVarOne = (double)varOne;
        System.out.println(newVarOne);
        int newVarTwo = (int)varTwo;
        System.out.println(newVarTwo);
        char charOne = 'M';
        System.out.println(charOne);
        charOne += 32;
        System.out.println(charOne);
    }
}
